    "在官方切分中，30 对奇数样本用于阈值校准，30 对偶数样本用于留出汇报：候选组件在原图条件下将 GPT-image2 召回率由 0.667 提升至 0.933，"
    "在微博下载条件下由 0.333 提升至 0.867，在小红书下载条件下由 0.667 提升至 0.933，三项真实图误报率均保持 0.000。"
